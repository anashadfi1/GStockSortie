    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String name = nomTxt.getText();
        String gmail = emailTxt.getText();
        String numTel = numTelTxt.getText();
        if(cs.create(new Client(name,numTel,gmail))){
            JOptionPane.showMessageDialog(this,"Bien ajout√©e");
            load();
        }
        
    }//GEN-LAST:event_addButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        String name = nomTxt.getText();
        String numTel = numTelTxt.getText();
        String gmail = emailTxt.getText();
        if(cs.update(new Client(id,name,numTel,gmail))){
            JOptionPane.showMessageDialog(this,"Successfully updated!");
            load();
        }
    }//GEN-LAST:event_updateButtonActionPerformed

    private void ClientsDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ClientsDataMouseClicked
        // TODO add your handling code here:
        id = Integer.parseInt(model.getValueAt(ClientsData.getSelectedRow(),0).toString());
        nomTxt.setText(model.getValueAt(ClientsData.getSelectedRow(),1).toString());
        numTelTxt.setText(model.getValueAt(ClientsData.getSelectedRow(),2).toString());
        emailTxt.setText(model.getValueAt(ClientsData.getSelectedRow(),3).toString());
    }//GEN-LAST:event_ClientsDataMouseClicked

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if(id!=0){
            int reponse = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete?");
            if(reponse == 0){
                cs.delete(cs.findById(id));
                load();
                JOptionPane.showMessageDialog(this,"Successfully deleted!");   
                    }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

